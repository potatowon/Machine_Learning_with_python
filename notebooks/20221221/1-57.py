# Euclidean Distance

v1, v2 = [1,2,3],[3,4,5]

diff_squate_sum = 0
for dim_idx in range(len(v2)):
    diff_squate_sum += (v1[dim_idx] - v2[dim_idx])**2
e_distance = diff_squate_sum**0.5
print(f'Euclidean distance between v1 and v2: {e_distance}')


